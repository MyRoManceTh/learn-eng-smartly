// Generate all lessons from modules
export const skillTreeLessons: SkillTreeLesson[] = skillTreeModules.flatMap((module) => {
  const topics = lessonTopics[module.id] || [];
  return topics.map((t, i) => ({
    id: `${module.id}-${i + 1}`,
    moduleId: module.id,
    topic: t.topic,
    topicThai: t.topicThai,
    order: i + 1,
  }));
});

// =============================================
// Helper functions
// =============================================

export function getModulesByPath(pathId: string): SkillTreeModule[] {
  return skillTreeModules.filter((m) => m.pathId === pathId);
}

export function getModulesByLevel(level: number): SkillTreeModule[] {
  return skillTreeModules.filter((m) => m.level === level);
}

export function getLessonsByModule(moduleId: string): SkillTreeLesson[] {
  return skillTreeLessons.filter((l) => l.moduleId === moduleId);
}

export function getModuleById(moduleId: string): SkillTreeModule | undefined {
  return skillTreeModules.find((m) => m.id === moduleId);
}

export function getPathById(pathId: string): SkillTreePath | undefined {
  return skillTreePaths.find((p) => p.id === pathId);
}

